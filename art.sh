clear_screen() {
    printf "\033c"
}

animate_art() {
    local shift=0
    local direction=1
    local max_shift=2
    local counter=0
    local max_iterations=$((3 * 5))  

    while [ $counter -lt $max_iterations ]; do
        clear_screen

       
        if [ $shift -ge $max_shift ]; then
            direction=-1
        elif [ $shift -le 0 ]; then
            direction=1
        fi
        shift=$((shift + direction))

        while IFS= read -r line; do
            printf "%${shift}s%s\n" "" "$line"
        done <<< "$ART"

        sleep 0.2
        counter=$((counter + 1))
    done
}

